    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if(validacion.isStringEmpty(this.txtDUI.getText())){
            JOptionPane.showMessageDialog(this, "Por favor ingrese su DUI");
            return;
        }
        
        if(validacion.isStringEmpty(this.txtPINp.getText())){
            JOptionPane.showMessageDialog(this, "Por favor ingrese su Pin");
            return;
        }
        
        Cliente cliente = new Cliente();
        String dui = this.txtDUI.getText();
        String pin = this.txtPINp.getText();
        
        ClienteController clienteC = new ClienteController();
        if(!clienteC.login(dui, pin, cliente)){
            JOptionPane.showMessageDialog(this, "Credenciales incorrectas");
            return;
        }
        
        Dashboard pantallaDashboard = new Dashboard(cliente.getId_cliente());
        dispose();
        pantallaDashboard.show();
    }//GEN-LAST:event_btnLoginActionPerformed

